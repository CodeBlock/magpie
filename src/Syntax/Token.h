#pragma once

#include <iostream>

#include "Macros.h"
#include "MagpieString.h"

namespace magpie
{
  class Memory;

  // The different types of Tokens that make up Magpie source code.
  enum TokenType
  {
    // Punctuators.
    TOKEN_LEFT_PAREN,
    TOKEN_RIGHT_PAREN,
    TOKEN_LEFT_BRACKET,
    TOKEN_RIGHT_BRACKET,
    TOKEN_LEFT_BRACE,
    TOKEN_RIGHT_BRACE,
    TOKEN_PLUS,
    TOKEN_MINUS,
    TOKEN_STAR,
    TOKEN_SLASH,

    // Keywords.
    TOKEN_CASE,
    TOKEN_DEF,
    TOKEN_DO,
    TOKEN_ELSE,
    TOKEN_FOR,
    TOKEN_IF,
    TOKEN_IS,
    TOKEN_MATCH,
    TOKEN_RETURN,
    TOKEN_THEN,
    TOKEN_WHILE,

    TOKEN_NAME,
    TOKEN_NUMBER,
    TOKEN_STRING,

    TOKEN_LINE,
    TOKEN_ERROR,
    TOKEN_EOF
  };

  // A single meaningful Token of source code. Generated by the Lexer, and
  // consumed by the Parser.
  class Token : public Managed
  {
  public:
    static temp<Token> create(TokenType type, gc<String> text);

    virtual size_t allocSize() const { return sizeof(Token); }

    TokenType     type() const { return type_; }
    const String& text() const { return *text_; }
    
    // Gets whether this token is of the given type.
    bool is(TokenType type) const { return type_ == type; }
    
  private:
    Token(TokenType type, const gc<String> text);

    TokenType   type_;
    gc<String>  text_;
  };

  std::ostream& operator <<(std::ostream& out, const Token& right);
}

